int							decodable_base64(const char *src, size_t len)
{
	if (len < 4)
	{
		if (!((*src == '=') || (*src >= 'a' && *src <= 'z') ||
				(*src >= 'A' && *src <= 'Z') ||
				(*src >= '0' && *src <= '9') ||
				*src == '+' || *src == '/'))
			return (1);
		*(char*)src = '=';
		return (0);
	}
	while (len--)
	{
		if (!((*src >= 'a' && *src <= 'z') ||
				(*src >= 'A' && *src <= 'Z') ||
				(*src >= '0' && *src <= '9') ||
				*src == '+' || *src == '/'))
			return (1);
	}
	return (0);
}

void						decode_base64(unsigned char *dst,
		const unsigned char *src, const size_t len, unsigned char c)
{
	unsigned int			i;

	i = -1;
	while (++i < (unsigned int)len)
	{
		if (src[i] == '=')
			break ;
		if ((c = g_base64de[(int)src[i]]) == 255)
		{
			src++;
			i -= 1;
			continue ;
		}
		if ((i & 0x3) == 0)
			*dst = (c << 2) & 0xFF;
		else if ((i & 0x3) == 1)
		{
			*dst++ |= (c >> 4) & 0x3;
			*dst = (c & 0xF) << 4;
		}
		else if ((i & 0x3) == 2)
		{
			*dst++ |= (c >> 2) & 0xF;
			*dst = (c & 0x3) << 6;
		}
		else
			*dst++ |= c;
	}
	*dst = 0;
}
